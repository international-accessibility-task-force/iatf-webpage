export function getFeaturedDraftCodes(languageCodes = []) {
  const availableCodes = new Set(languageCodes);
  return FEATURED_DRAFT_LANGUAGE_CODES.filter((code) => availableCodes.has(code));
}

export function getLanguageBrowserGroups(languageCodes = []) {
  const availableCodes = new Set(languageCodes);
  return LANGUAGE_BROWSER_GROUPS.map((group) => ({
    ...group,
    codes: group.codes.filter((code) => availableCodes.has(code))
  })).filter((group) => group.codes.length > 0);
}
